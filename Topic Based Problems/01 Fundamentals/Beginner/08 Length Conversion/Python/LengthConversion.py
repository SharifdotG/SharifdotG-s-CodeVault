meter = float(input("Enter meter: "))
print("Centimeter:", meter * 100)
print("Kilometer:", meter / 1000)
print("Mile:", meter / 1609.34)
print("Foot:", meter * 3.28084)
print("Inch:", meter * 39.3701)
