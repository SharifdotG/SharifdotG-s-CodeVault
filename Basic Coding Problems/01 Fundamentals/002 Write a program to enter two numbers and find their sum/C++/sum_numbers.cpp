#include <iostream>

using namespace std;

int main() {
    int first_number, second_number, sum;

    cout << "Enter the first number: ";
    cin >> first_number;

    cout << "Enter the second number: ";
    cin >> second_number;

    sum = first_number + second_number;

    cout << "The sum of the two numbers is: " << sum << endl;

    return 0;
}