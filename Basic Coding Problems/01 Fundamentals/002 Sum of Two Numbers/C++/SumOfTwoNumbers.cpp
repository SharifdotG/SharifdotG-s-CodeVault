#include <iostream>

using namespace std;

int main() {
    int firstNumber, secondNumber, sum;

    cout << "Enter the first number: ";
    cin >> firstNumber;

    cout << "Enter the second number: ";
    cin >> secondNumber;

    sum = firstNumber + secondNumber;

    cout << "The sum of the two numbers is: " << sum << endl;

    return 0;
}